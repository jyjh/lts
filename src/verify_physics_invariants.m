function expectedPower = assertBrakePowerCorner(forces, suffix, omegaPost, label)
    beforeField = ['brakeOmegaBefore_' suffix];
    rawField = ['brakeOmegaUnclamped_' suffix];
    avgField = ['brakeOmegaAvg_' suffix];
    torqueField = ['brakeTorque_' suffix];
    powerField = ['brakePower_' suffix];

    omegaBefore = forces.(beforeField);
    omegaUnclamped = forces.(rawField);
    omegaAvg = forces.(avgField);
    brakeTorque = forces.(torqueField);

    assert(isfinite(omegaBefore) && omegaBefore >= -1e-10, ...
        sprintf('%s %s invalid brake omega before', label, suffix));
    assert(isfinite(omegaUnclamped), ...
        sprintf('%s %s invalid unclamped brake omega', label, suffix));
    assert(isfinite(omegaAvg) && omegaAvg >= -1e-10, ...
        sprintf('%s %s invalid average brake omega', label, suffix));
    assertClose(omegaPost, max(omegaUnclamped, 0), 1e-10, ...
        sprintf('%s %s post brake omega telemetry', label, suffix));

    if omegaBefore > 0 && omegaUnclamped < 0
        activeFraction = omegaBefore / max(omegaBefore - omegaUnclamped, eps);
        expectedAvg = 0.5 * omegaBefore * activeFraction;
    else
        expectedAvg = 0.5 * (omegaBefore + max(omegaUnclamped, 0));
    end

    assertClose(omegaAvg, expectedAvg, 1e-10, ...
        sprintf('%s %s brake average omega telemetry', label, suffix));

    expectedPower = brakeTorque * omegaAvg;
    assertClose(forces.(powerField), expectedPower, 1e-10, ...
        sprintf('%s %s brake power telemetry', label, suffix));
end

