function x = staticDamperPosition(corner, load)
    load = max(load, 0);
    wheelRate = cornerWheelRate(corner);
    bumpRate = max(0, corner.bumpStopRate);
    bumpGap = max(0, corner.bumpStopLength);

    if load <= eps
        x = 0;
    elseif wheelRate <= eps
        if bumpRate > eps
            x = bumpGap + load / bumpRate;
        else
            x = 0;
        end
    elseif load <= wheelRate * bumpGap || bumpRate <= eps
        x = load / max(wheelRate, eps);
    else
        x = (load + bumpRate * bumpGap) / max(wheelRate + bumpRate, eps);
    end
end

function force = staticSuspensionForce(corner, damperPosition)
    wheelRate = cornerWheelRate(corner);
    bumpRate = max(0, corner.bumpStopRate);
    bumpGap = max(0, corner.bumpStopLength);
    force = wheelRate * damperPosition;
    if damperPosition > bumpGap
        force = force + bumpRate * (damperPosition - bumpGap);
    end
end

function assertAntiRollDistribution(vehicle, loads, label)
    W = vehicle.totalMass * 9.81;
    staticFront = W * vehicle.staticFrontWeight / 2;
    staticRear = W * (1 - vehicle.staticFrontWeight) / 2;
    halfTrack = vehicle.trackWidth / 2;

    frontRollMoment = ((loads.FR - staticFront) - (loads.FL - staticFront)) ...
        * halfTrack;
    rearRollMoment = ((loads.RR - staticRear) - (loads.RL - staticRear)) ...
        * halfTrack;
    totalRollMoment = frontRollMoment + rearRollMoment;
    frontShare = frontRollMoment / max(totalRollMoment, eps);

    assertClose(frontShare, vehicle.suspension.frontRollStiffDist, ...
        1e-10, sprintf('%s front roll stiffness distribution', label));
    assertClose(vehicle.suspension.frontLeft.state.antiRollForce + ...
        vehicle.suspension.frontRight.state.antiRollForce, 0, 1e-10, ...
        sprintf('%s front anti-roll net vertical force', label));
    assertClose(vehicle.suspension.rearLeft.state.antiRollForce + ...
        vehicle.suspension.rearRight.state.antiRollForce, 0, 1e-10, ...
        sprintf('%s rear anti-roll net vertical force', label));
    assertClose(loads.FL + loads.FR + loads.RL + loads.RR, W, ...
        1e-8, sprintf('%s total vertical load conservation', label));
end

function assertVerticalLoadConserved(vehicle, loads, label)
    assertVerticalLoadTotal(loads, vehicle.totalMass * 9.81, label);
end

function assertVerticalLoadTotal(loads, expectedTotalLoad, label)
    loadVector = [loads.FL; loads.FR; loads.RL; loads.RR];
    assert(all(loadVector >= -1e-10), ...
        sprintf('%s produced negative tire normal load', label));
    assertClose(sum(loadVector), expectedTotalLoad, 1e-8, ...
        sprintf('%s total vertical load', label));
end
