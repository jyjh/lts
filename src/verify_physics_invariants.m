function assertStepForceBalance(vehicle, oldState, newState, forces, label, trackHeading)
    if nargin < 6 || isempty(trackHeading)
        trackHeading = oldState.heading - oldState.headingError;
    end

    mass = vehicle.totalMass;
    expectedAx = forces.F_net_long / mass + oldState.vy * oldState.yawRate;
    aeroLat = utils.getStructField(forces, 'F_aero_lat', 0);
    dampingLat = utils.getStructField(forces, 'F_lateral_damping', 0);
    expectedAy = (forces.F_tire_lat + aeroLat + dampingLat) / mass;
    expectedGroundLong = forces.F_tire_long - forces.F_rollResist;
    dt = max(newState.time - oldState.time, eps);
    expectedVy = oldState.vy + ...
        (newState.ay - max(oldState.speed, 0) * oldState.yawRate) * dt;
    bodyLongitudinalDistance = integrateForwardDistance( ...
        oldState.speed, newState.ax, dt);
    bodyLongitudinalSpeed = bodyLongitudinalDistance / dt;
    meanVy = 0.5 * (oldState.vy + newState.vy);
    meanYawRate = 0.5 * (oldState.yawRate + newState.yawRate);
    oldHeadingError = oldState.headingError;
    oldLateralError = oldState.lateralError;
    curvatureDenominator = 1 - newState.curvature * oldLateralError;
    expectedPathSpeed = (bodyLongitudinalSpeed * cos(oldHeadingError) ...
        - meanVy * sin(oldHeadingError)) / curvatureDenominator;
    expectedLateralErrorRate = meanVy * cos(oldHeadingError) ...
        + bodyLongitudinalSpeed * sin(oldHeadingError);
    expectedHeadingError = oldHeadingError + ...
        (meanYawRate - expectedPathSpeed * newState.curvature) * dt;
    expectedHeadingError = atan2(sin(expectedHeadingError), cos(expectedHeadingError));

    assertClose(newState.ax, expectedAx, 1e-10, ...
        sprintf('%s longitudinal force balance', label));
    assertClose(newState.ay, expectedAy, 1e-10, ...
        sprintf('%s lateral force balance', label));
    assertClose(newState.vy, expectedVy, 1e-10, ...
        sprintf('%s lateral velocity integration', label));
    assertClose(forces.F_ground_long, expectedGroundLong, 1e-10, ...
        sprintf('%s ground longitudinal force split', label));
    assertClose(forces.chassisGroundPitchMoment, ...
        forces.F_ground_long * vehicle.cgHeight, 1e-10, ...
        sprintf('%s chassis ground pitch moment', label));
    assertClose(forces.chassisDragPitchMoment, ...
        forces.F_drag * utils.getStructField(forces, 'aeroDragHeight', 0), 1e-10, ...
        sprintf('%s chassis drag pitch moment', label));
    assertClose(forces.chassisGroundRollMoment, ...
        (forces.F_tire_lat + dampingLat) * vehicle.cgHeight, 1e-10, ...
        sprintf('%s chassis ground roll moment', label));
    assertClose(forces.chassisAeroRollMoment, ...
        utils.getStructField(forces, 'aeroRollMoment', 0), 1e-10, ...
        sprintf('%s chassis aero roll moment', label));
    assertClose(vehicle.chassis.state.lateralLoadTransfer, ...
        (forces.chassisGroundRollMoment + forces.chassisAeroRollMoment) ...
        / vehicle.trackWidth, 1e-10, ...
        sprintf('%s chassis lateral load transfer moment split', label));
    assertBrakeTorqueRadius(vehicle, forces, label);
    assertBrakePowerTelemetry(vehicle, forces, label);
    assertBrakeWheelPlaneSpeeds(vehicle, oldState, newState.steer, forces, label);
    assertChassisRatesSynced(vehicle, label);
    assertAppliedPowertrainState(vehicle, forces, label);
    assertPowerTelemetry(vehicle, forces, label);

    tireUsage = [
        vehicle.tire.FL.frictionUsage
        vehicle.tire.FR.frictionUsage
        vehicle.tire.RL.frictionUsage
        vehicle.tire.RR.frictionUsage
    ];
    assert(all(tireUsage <= 1 + 1e-10), ...
        sprintf('%s tire friction usage exceeded 1.0', label));
    assertTireForceWithinLimit(vehicle.tire.FL, sprintf('%s FL tire force limit', label));
    assertTireForceWithinLimit(vehicle.tire.FR, sprintf('%s FR tire force limit', label));
    assertTireForceWithinLimit(vehicle.tire.RL, sprintf('%s RL tire force limit', label));
    assertTireForceWithinLimit(vehicle.tire.RR, sprintf('%s RR tire force limit', label));
    assert(isfinite(newState.headingError), ...
        sprintf('%s produced non-finite heading error', label));
    assert(isfinite(newState.lateralError), ...
        sprintf('%s produced non-finite lateral path error', label));
    assert(isfinite(newState.trackProgressSpeed) && newState.trackProgressSpeed >= 0, ...
        sprintf('%s produced invalid path progress speed', label));
    assertClose(newState.s - oldState.s, newState.trackProgressSpeed * dt, 1e-10, ...
        sprintf('%s path progress kinematics', label));
    assertClose(newState.trackProgressSpeed, expectedPathSpeed, 1e-10, ...
        sprintf('%s path speed uses mean body velocity', label));
    assertClose(newState.lateralErrorRate, expectedLateralErrorRate, 1e-10, ...
        sprintf('%s lateral path error rate uses mean body velocity', label));
    assertClose(newState.lateralError, ...
        oldState.lateralError + expectedLateralErrorRate * dt, 1e-10, ...
        sprintf('%s lateral path error integration', label));
    assertClose(newState.headingError, expectedHeadingError, 1e-10, ...
        sprintf('%s heading error integration uses mean yaw rate', label));
    assertClose(newState.heading, trackHeading + newState.headingError, 1e-10, ...
        sprintf('%s vehicle heading equals track heading plus error', label));
    assert(abs(newState.lateralError) <= vehicle.trackHalfWidth, ...
        sprintf('%s left the track in a one-step invariant case', label));
    assert(forces.driveWheelTorqueApplied <= forces.driveWheelTorqueRequested + 1e-10, ...
        sprintf('%s applied more drive torque than requested', label));
    assert(forces.driveWheelTorqueForBrakeLimit <= ...
        forces.driveWheelTorqueRequested + 1e-10, ...
        sprintf('%s brake lock predictor used more drive torque than requested', label));
    assert(forces.driveWheelTorqueForBrakeLimit >= -1e-10, ...
        sprintf('%s brake lock predictor used negative drive torque', label));
    assert(newState.speed >= 0, sprintf('%s produced negative speed', label));
    assert(newState.s >= oldState.s, sprintf('%s moved backward along track', label));
end


function ds = integrateForwardDistance(initialSpeed, ax, dt)
    initialSpeed = max(initialSpeed, 0);
    if ax < 0 && initialSpeed > 0
        timeToStop = -initialSpeed / ax;
        if timeToStop < dt
            ds = initialSpeed * timeToStop + 0.5 * ax * timeToStop^2;
            ds = max(ds, 0);
            return;
        end
    end

    ds = max(0, initialSpeed * dt + 0.5 * ax * dt^2);
end

